package com.sagar.lld.model;

public class Cell {
	private int x;
	private int y;
	private String value;
	
	public Cell(String value) {
		this.value = value;
	}


	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
	
	

}
